# Operações Matematicas e Logicas

# Adição: a + b
print(2 + 3) # = 5

# Subtração: a - b
print(9 - 3) # = 6

#  Multiplicação: a * b
print(11 * 5) # = 55

# Divisão: a \ b
print(9 / 2) # = 4.5

# Divião por enteiros: a \\ b
print(9 // 2) # = 4

# Resto da divisão: a % b
print(5 % 2) # = 1

# Potenciação: a ** b
print(2 ** 4) # = 16

# 'E' lógico: a and b
print(True and True) # = True
print(True and False) # = False

# 'OU' lógico: a or b
print(True or True) # = True
print(True or False) # = True

# 'NÃO' lógico: not a
print(not True) # = False
print(not False) # = True

# Igualdade: a == b
print(2 == 2) # = True
print(2 == 3) # = False

# Diferença: a != b
print(2 != 2) # = False
print(2 != 3) # = True

# Maior que: a > b
print(3 > 2) # = True
print(2 > 2) # = False

# Menor que: a < b
print(2 < 2) # = False
print(2 < 3) # = True

# Maior igual que: a >= b
print(2 >= 2) # = True
print(4 >= 3) # = True

# Menor igual que: a <= b
print(2 <= 2) # = True
print(2 <= 3) # = True
